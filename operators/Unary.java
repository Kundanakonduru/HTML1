class Unary
{
public static void main(String[] args)
{
int i=4;
boolean flag=true;
System.out.println("Unary plus:"+(+i));
System.out.println("Unary minus:"+(-i));
System.out.println("Preincrement:"+(++i));
System.out.println("Postincrement:"+(i++));
System.out.println("Predecrement:"+(--i));
System.out.println("Postdecrement:"+(i--));
System.out.println("Not:"+(!flag));
}
}






